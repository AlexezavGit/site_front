export function getPCL5Severity(score: number): { label: string; color: string; route: string } {
  if (score < 20) return { label: "Нижче порогу ПТСР", color: "text-green-600", route: "no_intervention" };
  if (score <= 29) return { label: "Легкий рівень", color: "text-yellow-600", route: "self_help" };
  if (score <= 44) return { label: "Помірний ПТСР", color: "text-orange-600", route: "specialist" };
  if (score <= 59) return { label: "Тяжкий ПТСР", color: "text-red-700", route: "specialist" };
  return { label: "Дуже тяжкий ПТСР", color: "text-red-900", route: "psychiatry" };
}
